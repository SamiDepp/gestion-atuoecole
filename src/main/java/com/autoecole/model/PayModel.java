package com.autoecole.model;

public class PayModel {
	
	String candidat;
	String moniteur;
	
	String total;
	String nbheure;
	String nbabsecence ;
	
	
	public String getCandidat() {
		return candidat;
	}
	public void setCandidat(String candidat) {
		this.candidat = candidat;
	}
	public String getMoniteur() {
		return moniteur;
	}
	public void setMoniteur(String moniteur) {
		this.moniteur = moniteur;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getNbheure() {
		return nbheure;
	}
	public void setNbheure(String nbheure) {
		this.nbheure = nbheure;
	}
	public String getNbabsecence() {
		return nbabsecence;
	}
	public void setNbabsecence(String nbabsecence) {
		this.nbabsecence = nbabsecence;
	}

}
